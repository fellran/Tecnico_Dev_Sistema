# end (end - Fim do print)
print('Uma informação importante', 'importante', end='--\n')

# sep (sep - separador do print)
print('Uma informação importante', 'importante', sep='--')

# type verifica o tipo da variavel
# int
print(type(12))

# Float (Valores decimais ou ponto flutuantes)
print(type(1.2))

# String (Caracteres letras e numero e simbolos)
print(type('oi'))

# Booleano (Verdadeiro ou Falso)
print(type(True))